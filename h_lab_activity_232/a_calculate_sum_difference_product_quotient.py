# Input two numbers
a = float(input("Enter first number: "))
b = float(input("Enter second number: "))

# Calculations
sum_result = a + b
difference = a - b
product = a * b
quotient = a / b

# Output
print("Sum:", sum_result)
print("Difference (first - second):", difference)
print("Product:", product)
print("Quotient (first / second):", quotient)
