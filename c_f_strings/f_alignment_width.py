name = "Ram Thapa"
print(f"Author: {name:>20}")  # Right aligned with width 20
print(f"Author: {name:<20}")  # Left aligned with width 20
print(f"Author: {name:^20}")  # Center aligned with width 20
print(f"Author: {name:.>20}")  # Right aligned with '.' fill character
print(f"Author: {name:.<20}")  # Left aligned with '.' fill character
print(f"Author: {name:.^20}")  # Center aligned with '.' fill character
print(f"Author: {name:.^30}")  # Center aligned with '.' fill character and width 30
print(f"Author: {name:.^10}")  # Center aligned with '.' fill character and width 10
print(f"Author: {name:*>20}")  # Right aligned with '*' fill character
print(f"Author: {name:*<20}")  # Left aligned with '*' fill character
print(f"Author: {name:*^20}")  # Center aligned with '*' fill character
print(f"Author: {name:.^25}")  # Center aligned with '.' fill character and width 25
print(f"Author: {name:.<25}")  # Left aligned with '.' fill character and width 25
print(f"Author: {name:.>25}")  # Right aligned with '.' fill character and width 25
print(f"Author: {name:~^20}")  # Center aligned with '~' fill character
print(f"Author: {name:~<20}")  # Left aligned with '~' fill character
print(f"Author: {name:~>20}")  # Right aligned with '~' fill character
print(f"Author: {name:.^15}")  # Center aligned with '.' fill character and width 15
